void main() {
  //Arithmetic Operators
  print(10 + 10);
  print(20 - 10);
  print(20 / 2);
  print(50 % 5);
  print(2 * 10);
  //Logical Operators
  print(true && true);
  print(true && false);
  print(false && false);
  print(true || true);
  print(true || false);
  print(false || false);
  print(!true && !false);
  //Relational Operators
  print(10 > 5);
  print(10 >= 5);
  print(10 < 5);
  print(10 <= 5);
  print(10 == 5);
  print(10 != 5);
}
